// ============================================
// 非门（NOT Gate）
// 功能：仿真时使用正逻辑（便于观察波形），
//      烧录时使用负逻辑（适配硬件电平）
// ============================================

module not_gate (
    input  wire a,
    output wire out
);

`ifdef SIM
    // ========================================
    // 仿真模式：教科书标准非门
    // ========================================
    assign out = ~a;
`else
    // ========================================
    // 烧录模式：负逻辑适配（按下灭，松开亮）
    // 物理上按键低有效，LED低有效，取反即实现非门
    // ========================================
    assign out = ~a;
`endif

endmodule
