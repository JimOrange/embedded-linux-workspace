// ============================================
// 三八译码器 - 顶层适配层
// 功能：仿真时直通，烧录时适配硬件负逻辑
// 
// 硬件说明：
//   - in[2]、in[1] 来自按键（低电平有效），需取反
//   - in[0] 来自跳线帽（直接控制电平），不取反
//   - 数码管为共阳（低电平点亮），输出需取反
// ============================================

module decoder_3_8_top (
    input  wire [2:0] in,   // 物理输入
    output wire [7:0] out   // 物理输出
);

    wire [2:0] in_logic;
    wire [7:0] out_logic;

`ifdef SIM
    // ========================================
    // 仿真模式：正逻辑直通（波形干净）
    // ========================================
    assign in_logic = in;
    assign out      = out_logic;
`else
    // ========================================
    // 烧录模式：负逻辑适配
    //   - in[2]、in[1] 来自按键，取反（按下=0 → 逻辑1）
    //   - in[0] 来自跳线帽，不取反（直接控制）
    //   - 输出取反（数码管共阳，低电平点亮）
    // ========================================
    assign in_logic[2] = ~in[2];
    assign in_logic[1] = ~in[1];
    assign in_logic[0] = in[0];

    assign out = ~out_logic;
`endif

    // 调用门级核心译码器
    decoder_3_8_gate u_core (
        .in(in_logic),
        .out(out_logic)
    );

endmodule
