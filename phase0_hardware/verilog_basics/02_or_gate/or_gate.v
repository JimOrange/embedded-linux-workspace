// ============================================
// 或门（OR Gate）
// 功能：仿真时使用正逻辑（便于观察波形），
//      烧录时使用负逻辑（适配硬件电平）
// ============================================

module or_gate (
    input  wire a,
    input  wire b,
    output wire out
);

`ifdef SIM
    // ========================================
    // 仿真模式：教科书标准或门
    // ========================================
    assign out = a | b;
`else
    // ========================================
    // 烧录模式：负逻辑适配（按键按下为0，LED点亮为0）
    // ========================================
    assign out = ~(~a | ~b);
`endif

endmodule
